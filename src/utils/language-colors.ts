export const getLanguageColor = (language: string): string => {
  // Normalize the language key for lookup, like replacing spaces/special chars if needed,
  // but standard GitHub languages usually match exact keys or the fallback works.
  const normalized = language.replace(/[^a-zA-Z0-9+#-]/g, '-');
  return languageColors[language] || languageColors[normalized] || languageColors.Other;
};
